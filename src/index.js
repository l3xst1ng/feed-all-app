//funtion to display the donor's Thank you card
function showThankYou() {
  const form = document.getElementById("foodForm");
  const thankYouCard = document.getElementById("thankYouCard");
  thankYouCard.style.display = "block";

  // Display submitted details in the thank you card
  document.getElementById("firstNameDisplay").innerText = "First Name: " + form.firstName.value;
  document.getElementById("lastNameDisplay").innerText = "Last Name: " + form.lastName.value;
  document.getElementById("contactDisplay").innerText = "Contact: " + form.contact.value;
  document.getElementById("foodCategoryDisplay").innerText = "Food Category: " + form.foodCategory.value;
  document.getElementById("descriptionDisplay").innerText = "Description: " + form.description.value;
  document.getElementById("locationDisplay").innerText = "Location: " + form.location.value;

  
}













































































































































































































































































































































































































































































































